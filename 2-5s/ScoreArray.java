class ScoreArray{
  public static void main(String[] args){
    //配列の生成
    int[] point = new int[4];

    //配列に値を代入
    point[0] = 90;
    point[1] = 80;
    point[2] = 100;
    point[3] = 90;
    //値を表示
    System.out.println("1人目は" + point[0] + "点");
    System.out.println("2人目は" + point[1] + "点");
    System.out.println("3人目は" + point[2] + "点");
    System.out.println("4人目は" + point[3] + "点");
  }
}
